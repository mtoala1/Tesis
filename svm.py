from sklearn.preprocessing import label_binarize
from sklearn.metrics import roc_curve, auc
import numpy as np
import matplotlib.pyplot as plt

# Número de clases
n_classes = len(train_generator.class_indices)

# Binarizar etiquetas reales
y_test_bin = label_binarize(y_test, classes=np.arange(n_classes))

# Obtener probabilidades del modelo
y_score = svm.predict_proba(X_test)

# Calcular ROC y AUC para cada clase
fpr = dict()
tpr = dict()
roc_auc = dict()

for i in range(n_classes):
    fpr[i], tpr[i], _ = roc_curve(y_test_bin[:, i], y_score[:, i])
    roc_auc[i] = auc(fpr[i], tpr[i])

# Calcular ROC macro-promedio
all_fpr = np.unique(np.concatenate([fpr[i] for i in range(n_classes)]))

mean_tpr = np.zeros_like(all_fpr)
for i in range(n_classes):
    mean_tpr += np.interp(all_fpr, fpr[i], tpr[i])

mean_tpr /= n_classes

roc_auc_macro = auc(all_fpr, mean_tpr)

# Graficar
plt.figure(figsize=(8,6))

for i, class_name in enumerate(train_generator.class_indices.keys()):
    plt.plot(fpr[i], tpr[i], label=f"{class_name} (AUC = {roc_auc[i]:.2f})")

plt.plot(all_fpr, mean_tpr, linestyle='--', label=f"Macro-average (AUC = {roc_auc_macro:.2f})")

plt.plot([0,1], [0,1], 'k--')
plt.xlabel("Tasa de Falsos Positivos (FPR)")
plt.ylabel("Tasa de Verdaderos Positivos (TPR)")
plt.title("Curva ROC Multiclase - MobileNetV2 + SVM")
plt.legend()
plt.show()

