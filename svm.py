#Crear extractor de características

from tensorflow.keras.applications import MobileNetV2
from tensorflow.keras.models import Model
from tensorflow.keras.layers import GlobalAveragePooling2D

base_model = MobileNetV2(
    weights='imagenet',
    include_top=False,
    input_shape=(224,224,3)
)

x = base_model.output
x = GlobalAveragePooling2D()(x)

feature_extractor = Model(inputs=base_model.input, outputs=x)

#Extraer características

import numpy as np

def extract_features(generator):

    features = []
    labels = []

    for images, lbls in generator:
        feats = feature_extractor.predict(images)
        features.append(feats)
        labels.append(lbls)

        if len(features)*batch_size >= generator.samples:
            break

    features = np.vstack(features)
    labels = np.vstack(labels)

    return features, np.argmax(labels, axis=1)

#Aplicar extracción

X_train, y_train = extract_features(train_generator)
X_val, y_val = extract_features(val_generator)
X_test, y_test = extract_features(test_generator)

